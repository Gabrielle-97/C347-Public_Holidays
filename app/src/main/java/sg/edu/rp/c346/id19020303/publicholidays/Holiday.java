package sg.edu.rp.c346.id19020303.publicholidays;

public class Holiday {

    private String name, date;

    public Holiday(String name, String date) {
        this.name = name;
        this.date = date;
    }
    public String getName() {
        return name;
    }

    public String getDate() {
        return date;
    }

}
